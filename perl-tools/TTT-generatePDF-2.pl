package TTT4perl::PNGtools;

##  There are so many CRC32 algorithms, with different polynoms and pre- and postprocessing
##  so it is faster to implement the example algorithm from the standard
##  See: http://www.w3.org/TR/PNG/#D-CRCAppendix
our $crc32_table;

# build a precalculated table for 256 bytes for faster crc calculation
sub build_table
{
  my $table = [];
  for (my $n = 0; $n < 256; $n++) {
    my $c = $n;
    for ( my $k = 0; $k < 8; $k++ ) {
      $c = ($c >> 1 ) ^ (($c & 1) ? 0xEDB88320 : 0);
    }
    $table->[$n] = $c;
  }
  return $table;
}

# calculate CRC checksum for PNG
sub crc
{
  my $CRC = 0xFFFFFFFF;
  my ($data_ptr) = @_;
  if (!defined $crc32_table) {
    $crc32_table = build_table();  
  }
  my @bytes = unpack("C*", $$data_ptr);
  foreach my $byte (@bytes) {
    $CRC= $crc32_table->[($CRC ^ $byte) & 0xFF] ^ ($CRC >> 8);
  }
  return ($CRC ^ 0xFFFFFFFF);
}

## Create a pHYs-chunk for a given resolution, as GD has no option for that :-(
sub create_pHYs
{
	my $resolution = shift;
	my $res2meter = int(100/2.54*$resolution);
	my $chunk = pack( 'a4L>L>C', 'pHYs', $res2meter, $res2meter, 1);
	my $crc=PNGtools::crc(\$chunk);
	my $pHYs= pack 'L>a*L>', 9, $chunk, $crc;
	#for debug printf "pHYs(%u): %s\n",  length($pHYs), join (" ", unpack("(H2)*",$pHYs));
	return $pHYs
}	

