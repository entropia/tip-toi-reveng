# split 16-bit OID value into an array of 9 2-bit values
# [0] is the checksum, [1]..[9] the bits from MSB to LSB
# analog to the printed OID code, where [0] is top-left
# and [9] bottom-right
sub OID_val2arr {
	my $value = shift;
	my $checksum;
	my @OIDarr =(0,0,0,0,0,0,0,0,0);
  $checksum = (((($value >> 2) ^ ($value >> 8) ^ ($value >> 12) ^ ($value >> 14)) & 0x01)^0x01) << 1;
  $checksum |= ((($value) ^ ($value >> 4) ^ ($value >> 6) ^ ($value >> 10)) & 0x01);
	$OIDarr[0]=$checksum;
	for (my $i=1; $i<9; $i++) {
		$OIDarr[$i]=(($value & 0xC000 )>>14);
		$value = $value << 2;
	}	
	return @OIDarr;
}	

# generate on 24x24 pixel raster for a given OID
# 600dpi
sub OID_val2GD {
  my @OIDarr = OID_val2arr(shift);
  my $OID_img = new GD::Image(24,24);
  my $white = $OID_img->colorAllocate(255, 255, 255);
  my $black = $OID_img->colorAllocate(0  ,   0,   0);
  $OID_img->transparent($white);
  $OID_img->rectangle(0,0,23,23, $white);
  # set the frame pixels surrounding the data pixels
  for my $pixel ([0,0], [6,0], [12,0], [18,0], [0,6], [1,12], [0,18]) {
  	$OID_img->setPixel(@$pixel[0], @$pixel[1], $black);
  }	
  # set the 9 data pixels
  # like documented here: http://upload.querysave.de/code.html
  for (my $i=0; $i<9; $i++) {
	    my $row = int($i / 3)+1;
	    my $col = ($i % 3)+1;
	    my $deltaX = 1-(2*((($OIDarr[$i]&0x02)>>1)^(($OIDarr[$i]&0x01))));
	    my $deltaY = 1-(2*(($OIDarr[$i]&0x02)>>1));
	    $OID_img->setPixel(($col*6)+$deltaX, ($row*6)+$deltaY, $black);
  }
  return $OID_img;
}  

